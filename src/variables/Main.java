package variables;

public class Main {

	public static void main(String[] args) {
//		data type
//		boolean
//		byte
//		short
//		int
//		long
//		float
//		double
//		char
//		String  
		
//		primitive and reference data type
		
		int x ; // declaration int size 4 bytes
		
		x = 123; // assignment (= equal sign means assignment)
		
		int x1 = 12; // initialization 
		
		System.out.println(x);
		System.out.println(x1);
		
		byte x2 = 127; // size 1 byte
		System.out.println(x2);
		
		long x3 = 127l; // size 8 bytes
		System.out.println(x3);
		
		float x4 = 12.7f; // 4 bytes
		System.out.println(x4);
		
		double x5 = 12.123123123123127f; // 8 bytes
		System.out.println(x5);
		
		char c = '#'; // 2 bytes
		System.out.println(c);
		
		String s = "java"; // varies String is a class
		System.out.println(s);
				

	}

}
